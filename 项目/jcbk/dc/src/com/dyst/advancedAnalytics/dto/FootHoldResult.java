package com.dyst.advancedAnalytics.dto;

public class FootHoldResult {

	private String hphm;
	private String jcdid;
	private int times;
	private int total;
	
	public String getHphm() {
		return hphm;
	}
	public void setHphm(String hphm) {
		this.hphm = hphm;
	}
	public String getJcdid() {
		return jcdid;
	}
	public void setJcdid(String jcdid) {
		this.jcdid = jcdid;
	}
	public int getTimes() {
		return times;
	}
	public void setTimes(int times) {
		this.times = times;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
}
