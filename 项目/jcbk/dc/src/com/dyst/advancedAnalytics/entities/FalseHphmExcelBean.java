package com.dyst.advancedAnalytics.entities;

public class FalseHphmExcelBean {

	private String hphm;
	private String lrsh;
	public FalseHphmExcelBean() {
		super();
	}
	public FalseHphmExcelBean(String hphm, String lrsh) {
		super();
		this.hphm = hphm;
		this.lrsh = lrsh;
	}
	public String getHphm() {
		return hphm;
	}
	public void setHphm(String hphm) {
		this.hphm = hphm;
	}
	public String getLrsh() {
		return lrsh;
	}
	public void setLrsh(String lrsh) {
		this.lrsh = lrsh;
	}
	
}
